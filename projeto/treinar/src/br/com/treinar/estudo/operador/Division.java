package br.com.treinar.estudo.operador;

public class Division {

	public static void main(String[] args) {
		int i = 10;
		int j = 8;
		float resultado = (float) i / j;
		System.out.println(resultado);
		
		float g = 1.5f;
		
		resultado = i / g;
		System.out.println(resultado);
		
	}
	
}
